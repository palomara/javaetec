package calculo;


public class FuncoesMatematicas {
	
	public double soma (double a, double b) {
		double total = a + b;
		return total;
	}	
		public double subtracao(double c, double d){
			double total = c - d;
			return total;
		}
		
		public double multiplicacao(double e, double f){
			//double total = 0;
			double total = e * f;
			return total;
		}
		
		public double divisao(double g, double h){
			double total = g / h;
			return total;
		}
		public double maismenos(double i, double j){
			double total = i = - j ;
			return total;
		}
		public double raiz(double k) {
			double total = Math.sqrt(k);
			return total;
		}
		public double fracao (double m) {
			double total = 1/m;
			return total;
		}
	}
	
